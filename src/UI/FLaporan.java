    private void btn_cetakMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_cetakMouseClicked
        try {
                JasperDesign jd = JRXmlLoader.load("src/laporan/report1.jrxml");
                JasperReport jr  = JasperCompileManager.compileReport(jd);
                HashMap param = new HashMap();
                JasperPrint jp = JasperFillManager.fillReport(jr, null, DBConfig.getKoneksi());
                JasperViewer jv = new JasperViewer (jp, false);
                jv.setVisible(true);
            } catch (JRException ex) {
                Logger.getLogger(FLaporan.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btn_cetakMouseClicked

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.isEnabled()){
            btn_cetak.setEnabled(true);
            btn_hapus.setEnabled(true);
            try {
                int a = jTable1.getSelectedRow();
                txt_kode.setText(jTable1.getValueAt(a, 0).toString());
                String gg = jTable1.getValueAt(a, 1).toString();
                txt_tanggal.setText(jTable1.getValueAt(a, 2).toString());
                txt_persen.setText(jTable1.getValueAt(a, 3).toString()+"%");
                txt_grade.setText("Grade "+gg);
                String sql = "SELECT * FROM identifikasi WHERE kd_tuna ='"+ txt_kode.getText()+"'";
                Connection cn = DBConfig.getKoneksi();
                Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                if (rs.first()){
                    Blob blob = rs.getBlob("foto");
                    byte[] data = blob.getBytes(1, (int) blob.length());
                    Image gbr = ImageIO.read(new ByteArrayInputStream(data));
                    panelGambar4.setImage(gbr);
                }
            } catch (SQLException ex) {
                Logger.getLogger(FSampel.class.getName()).log(Level.SEVERE, null, ex);
        }   catch (IOException ex) {
                Logger.getLogger(FLaporan.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

