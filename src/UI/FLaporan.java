    private DefaultTableModel tampil() throws SQLException {
        Object header[] ={"Kode", "Grade", "Tanggal", "Persentasi"};
        DefaultTableModel tabel = new DefaultTableModel(null, header);
        String sql = "SELECT * FROM identifikasi WHERE ket='Layak Ekspor' order by kd_tuna asc";
        Connection conn = DBConfig.getKoneksi();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);
        while (rs.next()) {
            String a = rs.getString("kd_tuna");
            String b = rs.getString("grade");
            String c = rs.getString("tanggal");
            String d = rs.getString("persentasi");
            Blob blob = rs.getBlob("foto");
            byte[] data = blob.getBytes(1, (int) blob.length());
            String h = new String(data);
            String kolom[] = {a, b, c, d};
            tabel.addRow(kolom);
            jTable1.setModel(tabel);
        }
        return tabel;
    }
    
